def code_to_score(opponent_score_code:str, your_score_code:str) -> int:
    # Opponent score
    # 1 for Rock, 2 for Paper, and 3 for Scissors
    # A for Rock, B for Paper, and C for Scissors

    # Your score
    # 1  for Rock, 2 for Paper, and 3 for Scissors
    # X for Rock, Y for Paper, and Z for Scissors

    opponent_offset = 64
    your_offset = 87

    opponent_score = ord(opponent_score_code) - opponent_offset
    your_score = ord(your_score_code) - your_offset

    return opponent_score, your_score

def score_to_points(scores:tuple) -> int:
    # card value + winner get 6 points, loss = 0, tie = 3
    bonus = 0

    if scores[0] == scores[1]: # tie
        bonus = 3
    elif scores[0] == ROCK: # Rock
        bonus = 6 if scores[1] == PAPER else 0 # Paper beats Rock
    elif scores[0] == PAPER: # Paper
        bonus = 6 if scores[1] == SCISSORS else 0 # Scissors beats Paper
    else:
        bonus = 6 if scores[1] == ROCK else 0 # SR

    return scores[1] + bonus

def choice_to_outcome(scores:tuple) -> int:
    # X means you need to lose,
    # Y means you need to end the round in a draw,
    # and Z means you need to win.
    # card value + winner get 6 points, loss = 0, tie = 3

    pick = 0
    bonus = 0

    match scores[1]:
        case 1: # Need to Loose
           match scores[0]:
            case 1:  # Rock
                pick = SCISSORS
            case 2: # Paper
                pick = ROCK
            case 3: # Scissors
                pick = PAPER
           bonus = 0

        case 2: # Need Tie
            pick = scores[0]
            bonus = 3
        case 3: # Need to Win
            match scores[0]:
                case 1:  # Rock
                    pick = PAPER
                case 2:  # Paper
                    pick = SCISSORS
                case 3:  # Scissors
                    pick = ROCK
            bonus = 6

    return pick + bonus

